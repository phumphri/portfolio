    if value["type"] == "property":
    
        # Format the new entry.
        new_entry = {"key":value["key"], "owner":value["owner"]}

        # Determine if the color is in the dictionary.
        if value["color"] in color_groups.keys():

            # Append the new entry to an existing color.
            color_groups[value["color"]].append(new_entry)
        else:

            # Create a new entry in the color group.
            color_groups[value["color"]] = [new_entry]

